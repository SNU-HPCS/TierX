# Helper: validate array against valid options
# Usage: validate_array "param_name" "display_name" valid_options_array[@] values_array[@]
validate_array() {
    local param_name="$1"; local display_name="$2"
    shift 2
    local -a valid_opts=("${!1}"); shift
    local -a values=("${!1}")
    if [ ${#values[@]} -eq 0 ]; then
        echo "  ❌ ERROR: $param_name parameter is required"
        return 1
    fi
    for val in "${values[@]}"; do
        if [[ ! " ${valid_opts[*]} " =~ " $val " ]]; then
            echo "  ❌ ERROR: Invalid $display_name '$val'. Valid options: ${valid_opts[*]}"
            return 1
        fi
        echo "  🦕 Valid $display_name: $val"
    done
    return 0
}

validate_params() {
    local VALIDATION_PASSED=true
    local VALID_APPS=("NN" "Seizure" "SpikeSorting" "GRU")
    local VALID_METRICS=("throughput" "operatingtime" "latency" "implant_power")
    local VALID_SWEEPS=("communication" "power" "node")
    local VALID_COMM=("HIGH-BCC" "LOW-BCC" "LOW-RF")
    local VALID_POWER=("OFF" "SMALL-CAP" "BAT" "SMALL-BAT")
    local VALID_NODES=("NECK-ARM" "NECK-EXTERNAL" "TEMPLE-ARM")

    validate_array "APPLICATIONS" "application" VALID_APPS[@] APPLICATIONS[@] || VALIDATION_PASSED=false
    validate_array "OPTIMIZE_METRICS" "optimization metric" VALID_METRICS[@] OPTIMIZE_METRICS[@] || VALIDATION_PASSED=false
    validate_array "SWEEP_TYPES" "sweep type" VALID_SWEEPS[@] SWEEP_TYPES[@] || VALIDATION_PASSED=false
    validate_array "COMMUNICATION_METHODS" "communication method" VALID_COMM[@] COMMUNICATION_METHODS[@] || VALIDATION_PASSED=false
    validate_array "POWER_SOURCES" "power source" VALID_POWER[@] POWER_SOURCES[@] || VALIDATION_PASSED=false
    validate_array "NODE_PLACEMENTS" "node placement" VALID_NODES[@] NODE_PLACEMENTS[@] || VALIDATION_PASSED=false
    if [ "$VALIDATION_PASSED" = false ]; then
        echo ""
        echo -e "\\033[31m❌ VALIDATION FAILED: Please fix the errors above before continuing.\\033[0m"
        exit 1
    else
        echo ""
        echo -e "\\033[32m✅ All parameters validated successfully!\\033[0m"
    fi
}

# Function to run commands with error handling and optional verbosity
run_command() {
    local cmd="$1"
    local desc="$2"
    
    if [ "$VERBOSE" = true ]; then
        echo "    Command: $cmd"
        if eval "$cmd"; then
            echo "    🦕 Completed: $desc"
        else
            echo "    ❌ Failed: $desc"
            exit 1
        fi
    else
        echo -n "    ⚙️  $desc... "
        if eval "$cmd" > /dev/null 2>&1; then
            echo "🦕 Done"
        else
            echo "❌ Failed"
            echo "    Error running: $cmd"
            exit 1
        fi
    fi
}

