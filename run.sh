            node) component_type="env" ;;
            *)
                echo "  ❌ ERROR: Invalid sweep type '$sweep_type'. Valid options: communication, power, node"
                exit 1
                ;;
        esac

        for opt in "${optimize_metric[@]}"; do
            if [ "$graph_only" = false ]; then
                total_dse_runs=$((total_dse_runs + 1))
                run_command "python src/DSE.py ${application} ${component_type} ${opt} ${DSE_FLAGS_COMMON}" "Running ${sweep_type} DSE for ${application} (${opt})"
            fi

            if [ "$SAVE_GRAPHS" = true ]; then
                echo -n "    ⚙️  Generating plots for ${application} ${component_type} ${opt}... "
                if [ "$VERBOSE" = true ]; then
                    echo ""
                    echo "    Command: python src/Plot_graph.py ${application} ${component_type} ${opt} \"${workloads[@]}\""
                    if python src/Plot_graph.py ${application} ${component_type} ${opt} "${workloads[@]}"; then
                        echo "    🦕 Completed: Generating plots for ${application} ${component_type} ${opt}"
                    else
                        echo "    ❌ Failed: Generating plots for ${application} ${component_type} ${opt}"
                        exit 1
                    fi
                else
                    if python src/Plot_graph.py ${application} ${component_type} ${opt} "${workloads[@]}" > /dev/null 2>&1; then
                        echo "🦕 Done"
                    else
                        echo "❌ Failed"
                        echo "    Error running: python src/Plot_graph.py ${application} ${component_type} ${opt} \"${workloads[@]}\""
                        exit 1
                    fi
                fi
            else
                echo "    ⚙️  Skipping plots (save-graphs disabled)"
            fi
        done
    done

    # Reset run.yaml between applications
    echo -e "# Configuration file for the simulation run\n" > src/run.yaml
    echo "workloads: " >> src/run.yaml
done


