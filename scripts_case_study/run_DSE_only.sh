validate_params() {
    local VALIDATION_PASSED=true

    # Check applications
    VALID_APPS=("NN" "Seizure" "SpikeSorting" "GRU")
    if [ -z "${APPLICATIONS}" ]; then
        echo "  ❌ ERROR: APPLICATIONS parameter is required"
        VALIDATION_PASSED=false
    else
        for app in "${APPLICATIONS[@]}"; do
            if [[ ! " ${VALID_APPS[@]} " =~ " $app " ]]; then
                echo "  ❌ ERROR: Invalid application '$app'. Valid options: ${VALID_APPS[@]}"
                VALIDATION_PASSED=false
            else
                echo "  🦕 Valid application: $app"
            fi
        done
    fi

    # Check optimization metrics
    VALID_METRICS=("throughput" "operatingtime" "latency" "implant_power")
    if [ -z "${OPTIMIZE_METRICS}" ]; then
        echo "  ❌ ERROR: OPTIMIZE_METRICS parameter is required"
        VALIDATION_PASSED=false
    else
        for metric in "${OPTIMIZE_METRICS[@]}"; do
            if [[ ! " ${VALID_METRICS[@]} " =~ " $metric " ]]; then
                echo "  ❌ ERROR: Invalid optimization metric '$metric'. Valid options: ${VALID_METRICS[@]}"
                VALIDATION_PASSED=false
            else
                echo "  🦕 Valid optimization metric: $metric"
            fi
        done
    fi

    # Check sweep types
    VALID_SWEEPS=("communication" "power" "node")
    if [ -z "${SWEEP_TYPES}" ]; then
        echo "  ❌ ERROR: SWEEP_TYPES parameter is required"
        VALIDATION_PASSED=false
    else
        for sweep in "${SWEEP_TYPES[@]}"; do
            if [[ ! " ${VALID_SWEEPS[@]} " =~ " $sweep " ]]; then
                echo "  ❌ ERROR: Invalid sweep type '$sweep'. Valid options: ${VALID_SWEEPS[@]}"
                VALIDATION_PASSED=false
            else
                echo "  🦕 Valid sweep type: $sweep"
            fi
        done
    fi

    # Check communication methods
    VALID_COMM=("HIGH-BCC" "LOW-BCC" "LOW-RF")
    if [ -z "${COMMUNICATION_METHODS}" ]; then
        echo "  ❌ ERROR: COMMUNICATION_METHODS parameter is required"
        VALIDATION_PASSED=false
    else
        for comm in "${COMMUNICATION_METHODS[@]}"; do
            if [[ ! " ${VALID_COMM[@]} " =~ " $comm " ]]; then
                echo "  ❌ ERROR: Invalid communication method '$comm'. Valid options: ${VALID_COMM[@]}"
                VALIDATION_PASSED=false
            else
                echo "  🦕 Valid communication method: $comm"
            fi
        done
    fi

    # Check power sources
    VALID_POWER=("OFF" "SMALL-CAP" "BAT" "SMALL-BAT")
    if [ -z "${POWER_SOURCES}" ]; then
        echo "  ❌ ERROR: POWER_SOURCES parameter is required"
        VALIDATION_PASSED=false
    else
        for power in "${POWER_SOURCES[@]}"; do
            if [[ ! " ${VALID_POWER[@]} " =~ " $power " ]]; then
                echo "  ❌ ERROR: Invalid power source '$power'. Valid options: ${VALID_POWER[@]}"
                VALIDATION_PASSED=false
            else
                echo "  🦕 Valid power source: $power"
            fi
        done
    fi

    # Check node placements
    VALID_NODES=("NECK-ARM" "NECK-EXTERNAL" "TEMPLE-ARM")
    if [ -z "${NODE_PLACEMENTS}" ]; then
        echo "  ❌ ERROR: NODE_PLACEMENTS parameter is required"
        VALIDATION_PASSED=false
    else
        for node in "${NODE_PLACEMENTS[@]}"; do
            if [[ ! " ${VALID_NODES[@]} " =~ " $node " ]]; then
                echo "  ❌ ERROR: Invalid node placement '$node'. Valid options: ${VALID_NODES[@]}"
                VALIDATION_PASSED=false
            else
                echo "  🦕 Valid node placement: $node"
            fi
        done
    fi

    if [ "$VALIDATION_PASSED" = false ]; then
        echo ""
        echo -e "\\033[31m❌ VALIDATION FAILED: Please fix the errors above before continuing.\\033[0m"
        exit 1
    else
        echo ""
        echo -e "\\033[32m✅ All parameters validated successfully!\\033[0m"
    fi
}

# Function to run commands with error handling and optional verbosity
run_command() {
    local cmd="$1"
    local desc="$2"
    
    if [ "$VERBOSE" = true ]; then
        echo "    Command: $cmd"
        if eval "$cmd"; then
            echo "    🦕 Completed: $desc"
        else
            echo "    ❌ Failed: $desc"
            exit 1
        fi
    else
        echo -n "    ⚙️  $desc... "
        if eval "$cmd" > /dev/null 2>&1; then
            echo "🦕 Done"
        else
            echo "❌ Failed"
            echo "    Error running: $cmd"
            exit 1
        fi
    fi
}

